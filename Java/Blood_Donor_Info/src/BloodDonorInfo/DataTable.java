    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        try {
            DefaultTableModel tableModel = (DefaultTableModel) table.getModel();
            int row = table.getSelectedRow();
            String number = tableModel.getValueAt(row, 1).toString();
            if (row >= 0) {
                int x = JOptionPane.showConfirmDialog(null, "Do you want to delete this selected info ?", "Deleting Confirmation", JOptionPane.YES_NO_OPTION);
                if (x == JOptionPane.YES_OPTION) {
                    String num = "DELETE FROM DonorInfo WHERE Number=" + number;
                    PreparedStatement pst = con.prepareStatement(num);
                    pst.execute();
                    pst.close();
                    refresh();
                }

            } else {
                JOptionPane.showMessageDialog(null, "Please select a row !");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Please select a row !");
        }
    }//GEN-LAST:event_deleteActionPerformed

