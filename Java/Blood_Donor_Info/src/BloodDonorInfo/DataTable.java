    private void insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertActionPerformed
        try {
            String name = donorName.getText();
            String number = donorNumber.getText();
            String blood = donorBlood.getText();
            String gender;
            if (donorGender.getSelectedIndex() == 0) {
                gender = "Male";
            } else {
                gender = "Female";
            }
            String address = donorAddress.getText();
            if (name.isEmpty() || number.isEmpty() || blood.isEmpty() || address.isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please fill-up information !");
            } else {
                PreparedStatement pst = con.prepareStatement("INSERT INTO DonorInfo VALUES(?,?,?,?,?)");
                pst.setString(1, name);
                pst.setString(2, number);
                pst.setString(3, blood);
                pst.setString(4, gender);
                pst.setString(5, address);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Insertion successful !");
                pst.close();

            }
            refresh();
            clear();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_insertActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        try {
            DefaultTableModel tableModel = (DefaultTableModel) table.getModel();
            int row = table.getSelectedRow();
            if (row >= 0) {
                String name = (String) tableModel.getValueAt(row, 0);
                String number = tableModel.getValueAt(row, 1).toString();
                String blood = (String) tableModel.getValueAt(row, 2);
                String address = (String) tableModel.getValueAt(row, 4);
                donorName.setText(name);
                donorNumber.setText(number);
                donorBlood.setText(blood);
                donorAddress.setText(address);
            } else {
                JOptionPane.showMessageDialog(null, "Please select a row !");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_editActionPerformed

