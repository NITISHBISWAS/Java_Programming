    private void pointTfKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pointTfKeyPressed
        char c = evt.getKeyChar();
        if (Character.isAlphabetic(c)) {
            JOptionPane.showMessageDialog(null, "Please enter your grade point!");
            pointTf.setText("");
        }
    }//GEN-LAST:event_pointTfKeyPressed

    private void AddBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddBtnActionPerformed
        code = codeTf.getText();
        point = pointTf.getText();

        if (code.equals("") || point.equals("")) {
            JOptionPane.showMessageDialog(null, "Please fill in all the rows!");
        } else {
            ta.append(code + "     " + "     " + point + "\n");
            totalPoint = totalPoint + Double.parseDouble(point);
            totalSemi++;
            codeTf.setText("");
            pointTf.setText("");
        }
    }//GEN-LAST:event_AddBtnActionPerformed

    private void ResultBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ResultBtnActionPerformed
        if (!ta.getText().equals("")) {
            cgpa = totalPoint / totalSemi;
            if (cgpa == 0) {
                result1.setText("F");
            } else if (cgpa > 0 && cgpa <= 1.0) {
                result1.setText("D");
            } else if (cgpa > 1.0 && cgpa <= 1.5) {
                result1.setText("C");
            } else if (cgpa > 1.5 && cgpa <= 2.0) {
                result1.setText("C+");
            } else if (cgpa > 2.0 && cgpa <= 2.5) {
                result1.setText("B");
            } else if (cgpa > 2.5 && cgpa <= 3.0) {
                result1.setText("B+");
            } else if (cgpa > 3.0 && cgpa <= 3.5) {
                result1.setText("A");
            } else if (cgpa > 3.5 && cgpa <= 4) {
                result1.setText("A+");
            } else if (cgpa > 4) {
                result1.setText("A+");
            }
            result.setText(String.valueOf(String.format("%.2f", cgpa)));

        }
    }//GEN-LAST:event_ResultBtnActionPerformed

